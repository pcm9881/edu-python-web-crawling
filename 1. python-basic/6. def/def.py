# 정의, 정의하다 definition, define


# 더하기
def plus(a, b):
    return a + b


# 빼기
def minus(a, b):
    return a - b


# 나누기
def divided_by(a, b):
    return a / b


# 곱하기
def multiplied_by(a, b):
    return a * b


print(plus(1, 2))  # 3

print(minus(5, 2))  # 3

print(divided_by(10, 2))  # 5

print(multiplied_by(3, 3))  # 9
